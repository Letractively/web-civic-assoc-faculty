	<p class="page_rules_sec_title"> PODĽA ZÁKONA č. 83/1990 Zb. O ZDRUŽOVANÍ OBČANOV V ZNENÍ NESKORŠÍCH PREDPISOV </p>
	 
	<p class="page_rules"> <b> Úvodné ustanovenie </b> </p>
	<p class="page_rules"> <b> a) </b> Názov združenia:  alumni FMFI </p>
	<span style="margin-left: 25px;"> Charakteristika: združenie študentov, zamestnancov, absolventov a priateľov FMFI UK </span> 
	<p class="page_rules"> <b> b) </b> Sídlo združenia:  FMFI UK, Mlynská dolina F1, 842 48 Bratislava </p> <br />
	 
	<p class="page_rules"> <b> c) Ciele a úlohy združenia </b> </p>
	<p class="page_rules"> 
		Členovia občianskeho združenia alumni FMFI (daľej o.z.) si uvedomujú význam rozvoja vedy a vzdelania pre rozvoj vzdelanostnej úrovne regiónu a jeho dosah na celkový hospodársky, ekonomický, kultúrny a spoločenský rozvoj. Hlavným cieľom o.z. je podpora všestranného rozvoja Fakulty matematiky, fyziky a informatiky Univerzity Komenského v Bratislave (ďalej FMFI UK) a to najmä v nasledujúcich oblastiach:
		
		<ul>
			<li> podpora vzdelávania, </li>
			<li> podpora a rozvoj telesnej kultúry, podpora  športu detí a mládeže (študentov fakulty,  detí zamestnancov, absolventov  a členov združenia, ...), </li>
			<li> zachovanie kultúrnych hodnôt, </li>
			<li> poskytovanie sociálnej pomoci a podpora zdravotne postihnutých občanov, </li>
			<li> podpora študentských aktivít, </li>
			<li> organizácia odborných, kultúrnych a spoločenských podujatí, </li>
			<li> podpora mobility študentov,  učiteľov a zamestnancov, </li>
			<li> vydavateľská a konzultačná činnosť, </li>
			<li> vytváranie podmienok pre trvalú existenciu intelektuálneho akademického prostredia na pracoviskách FMFI UK, </li>
			<li> príprava ďalších projektov, </li>
			<li> podpora vedy a výskumu a ich prepojenie s praxou, </li>
			<li> spolupráca s vysokými školami a odbornými inštitúciami v Slovenskej republike a v zahraničí, </li>
			<li> spolupráce so základnými a strednými školami, </li>
		</ul> 
		
		Občianske združenie chce dosiahnuť svoje ciele prostredníctvom aktivít smerujúcich k úzkej spolupráci FMFI UK s orgánmi regionálnej a miestnej správy a samosprávy, k spolupráci s podnikateľskými a finančnými subjektami a masmédiami, ako aj prostredníctvom získavania finančných prostriedkov a darov.
	</p>
	
	<p class="page_rules"> <b> d) Členstvo v združení </b> </p>
	<p class="page_rules"> 
		Členstvo v združení môže byť:
		
		<ul>
			<li> <i> <u> <b> Čestné </b> </u> </i> – môže potvrdiť členská schôdza na návrh výkonnej rady alebo štatutára združenia tomu, kto sa významným spôsobom zaslúžil o rozvoj združenia alebo FMFI UK. </li>
			<li> <i> <u> <b> Prispievateľské </b> </u> </i> – fyzická alebo právnická osoba, ktorá riadne zaplatila členský príspevok vo výške minimálne 5€. Členstvo vzniká prijatím členského príspevku a zaniká k 31.12. v kalendárnom roku, keď člen nezaplatil členský príspevok. </li>
			<li> <i> <u> <b> Činné </b> </u> </i> – osoba aktívne sa podieľajúca na aktivitách občianskeho združenia. Členstvo v združení vzniká prijatím člena výkonnou radou na základe písomnej žiadosti uchádzača o členstvo. </li>
		</ul>
		
		<i> <u> <b> Práva členov: </b> </u> </i>
		<ul> 
			<li> podieľať sa na činnosti združenia, </li>
			<li> voliť a byť volený do orgánov združenia, </li>
			<li> obracať sa na orgány združenia s námetmi a sťažnosťami a žiadať o stanovisko, </li>
			<li> byť informovaný o činnosti a o rozhodnutiach orgánov združenia. </li>
		</ul>

		<i> <u> <b> Povinnosti členov </b> </u> </i>
		<ul>
			<li> dodržiavať stanovy združenia, </li>
			<li> pomáhať pri plnení cieľov združenia a aktívne sa podieľať na jeho práci, </li>
			<li> podľa svojho svedomia, rozsahu svojich možností a schopností pomáhať orgánom združenia, </li>
			<li> platiť členské príspevky, </li>
			<li> ochraňovať a zveľaďovať majetok združenia. </li>
		</ul>
			
		<i> <u> <b> Zánik členstva </b> </u> </i>
		<ul>		
			<li> vystúpením – členstvo zaniká dňom doručenia písomného oznámenia člena o vystúpení zo združenia, </li>
			<li> úmrtím fyzickej osoby, resp. zánikom právnickej osoby, </li>
			<li> zánikom združenia, </li>
			<li> vyškrtnutím z dôvodu nečinnosti člena (týka sa činných členov), </li>
			<li> k 31.12. v kalendárnom roku keď nebol zaplatený členský príspevok, </li>
			<li> vylúčením, ak člen opätovne a napriek výstrahe porušuje členské povinnosti, alebo z iných dôležitých dôvodov. O podmienečnom vylúčení rozhoduje výkonná rada. Proti rozhodnutiu o vylúčení má právo podať člen odvolanie na členskú schôdzu, ktorá potvrdzuje rozhodnutie nadpolovičnou väčšinou. </li>
		</ul>
	</p>
	
	<p class="page_rules">
		<b> e) Organizačná štruktúra združenia </b>
		<ul style="list-style: decimal;">
			<li> najvyšší orgán - členská schôdza - delegáti členskej základne, </li>
			<li> výkonný orgán  - výkonná rada – 3 členná spomedzi činných členov o.z. – 2 členov menuje a odvoláva dekan FMFI UK, 1 člena volí a odvoláva členská schôdza, </li>
			<li> štatutárny orgán – predseda výkonnej rady – volia si ho spomedzi seba členovia výkonnej rady, </li>
			<li> kontrolný orgán - revízna komisia - 3 členná spomedzi  činných, prispievateľských členov - navrhuje a schvaľuje členská schôdza. </li>
		</ul>
		Funkčné obdobie orgánov združenia je štandardne 4 roky. Zvolený (menovaný) člen výkonnej rady a revíznej komisie zastáva svoju funkciu pokiaľ nie je zvolený (menovaný) nový člen. V prípade ukončenie výkonu funkcie (z dôvodu napr. odstúpenia, úmrtia, zrušenia členstva,...) nového dočasného vykonávateľa funkcie do najbližšej členskej schôdze menuje dekan FMFI UK. Funkcie vo výkonnej rade a revíznej komisií sú navzájom nezlučiteľné.
		Prípravný výbor sa vznikom o.z. automaticky stáva výkonnou radou (okrem dekana) a má povinnosť do pol roka od vzniku o.z. zvolať prvú členskú schôdzu.
	</p>
	 
	<p class="page_rules">
		<b> f) Spôsob ustanovenia orgánov a ich právomoci </b> <br />
		<span style="margin-left: 10px;"> <b> 1) <i> <u> Členská schôdza </u> </i> </b> o.z. je tvorená z delegátov členskej základne </span>
		<ul>		
			<li> delegátom členskej základne je každý člen o.z., ktorý sa účastní členskej schôdze, </li>
			<li> pravidelná výročná členská schôdza je zvolávaná výkonnou radou raz ročne, </li>
			<li> mimoriadnu členskú schôdzu môže navrhnúť každý člen o.z. na základe písomnej žiadosti výkonnej rade podpísanej 20 členmi o.z. , </li>
			<li> členská schôdza volí tretieho člena výkonnej rady nadpolovičnou väčšinou prítomných členov, </li>
			<li> volí a odvoláva členov revíznej komisie, </li>
			<li> rozhoduje o zániku združenia zlúčením s iným občianskym združením alebo dobrovoľným rozpustením, </li>
			<li> schvaľuje plán činnosti a výročnú správu, </li>
			<li> schvaľuje rozpočet a správu o hospodárení, </li>
			<li> potvrdzuje prijatie nových členov a vylúčenie členov. </li>
		</ul>
		 
		<span style="margin-left: 10px;"> <b> 2) <i> <u> Výkonná rada </u> </i> </b> </span>
		<ul>
			<li> je za svoju činnosť zodpovedná členskej schôdzi, </li>
			<li> riadi činnosť združenia, </li>
			<li> schvaľuje stanovy, ich zmeny a doplnky, </li>
			<li> zvoláva a obsahovo pripravuje rokovanie najvyššieho orgánu a pripravuje základné materiály na tieto rokovania, </li>
			<li> zvoláva výročnú členskú schôdzu – minimálne raz ročne, kde predkladá výročnú správu o činnosti o.z., </li>
			<li> je povinná zvolať mimoriadnu členskú schôdzu, najneskôr do 30 dní od obdržania písomnej žiadosti o jej konanie. Má však možnosť posunúť termín mimoriadnej členskej schôdze tak, aby časový odstup od poslednej členskej schôdze bol aspoň tri mesiace, </li>
			<li> schvaľuje prijatie nových činných členov. </li>
		</ul>
		
		<span style="margin-left: 10px;"> <b> 3) <i> <u> Štatutárnym orgánom </u> </i> </b> združenia je predseda výkonnej rady. </span> <br />
		 
		<span style="margin-left: 10px;"> <b> 4) <i> <u> Revízna komisia </u> </i> </b> </span>
		<ul>
			<li> je kontrolným orgánom, ktorý za svoju činnosť zodpovedá členskej základni, </li>
			<li> predkladá výročnú správu o kontrole činnosti o.z. minimálne raz ročne na výročnej členskej schôdzi, </li>
			<li> kontroluje hospodárenie združenia, upozorňuje orgány združenia na nedostatky a navrhuje opatrenia na ich odstránenie, </li>
			<li> kontroluje aj dodržiavanie stanov a vnútorných predpisov, </li>
			<li> spomedzi seba volia členovia revíznej komisie predsedu revíznej komisie, </li>
		</ul>
	</p>
	
	<p class="page_rules">
		<b> g) Zásady hospodárenia </b>
		<ul>
			<li> hospodárenie sa uskutočňuje podľa schváleného rozpočtu, </li>
			<li> združenie hospodári s hnuteľným a nehnuteľným majetkom (ak ho má), </li>
			<li> zdrojmi majetku sú: členské príspevky, dary od fyzických osôb, dary,  dotácie a granty od právnických osôb, </li>
			<li> výnosy z majetku a vlastnej činnosti môžu byt použité len na podporu cieľa združenia, </li>
			<li> V záujme vytvárania vlastných zdrojov môže združenie vykonávať v doplnkovom rozsahu vo vzťahu k záujmovej činnosti podnikateľskú činnosť, súvisiacu so zabezpečovaním cieľov a poslania združenia, a v súlade so všeobecne záväznými predpismi a stanovami. </li>
		</ul>
	</p>
	
	<p class="page_rules"> 
		<b> h) Zánik združenia </b> <br />
		O zániku združenia zlúčením s iným občianskym združením alebo dobrovoľným rozpustením rozhoduje členská schôdza, ktorá menuje likvidátora. Likvidátor najskôr vyrovná všetky záväzky a pohľadávky a s likvidačným zostatkom naloží podľa rozhodnutia výkonnej rady. Zánik združenia treba oznámiť do 15 dní po ukončení likvidácie Ministerstvu vnútra Slovenskej republiky. Pri likvidácii združenia sa primerane postupuje podľa §70 – 75 Obchodného zákonníka.
	</p>
	
